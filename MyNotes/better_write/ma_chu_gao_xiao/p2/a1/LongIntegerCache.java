public class LongIntegerCache {

    public static void main(String[] args) {

        Long a = 127L;

        Long b = 127L;

        System.out.println(a == b);

        System.out.println("");

        Long a1 = 128L;

        Long b1 = 128L;

        System.out.println(a1 == b1);

        System.out.println("");

        Long c = -128L;

        Long d = -128L;

        System.out.println(c == d);

        System.out.println("");

        Long c1 = -129L;

        Long d1 = -129L;

        System.out.println(c1 == d1);

        System.out.println("");

        Long e = 1000L;

        Long f = 1000L;

        System.out.println(e == f);

        System.out.println("");



    }
}
